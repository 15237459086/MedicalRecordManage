package com.kurumi.pojo.coding;

public class InfusionBloodElement {

	/**
	 * 血液成分编号
	 */
	private String bloodElementCode;
	
	/**
	 * 血液成分名称
	 */
	private String bloodElementName;
	
	/**
	 * 血液成分含量
	 */
	private String bloodElementVolume;
	
	/**
	 * 血液成分单位Code
	 */
	private String bloodElementUnitCode;
	
	/**
	 * 血液成分单位Name
	 */
	private String bloodElementUnitName;

	public String getBloodElementCode() {
		return bloodElementCode;
	}

	public void setBloodElementCode(String bloodElementCode) {
		this.bloodElementCode = bloodElementCode;
	}

	public String getBloodElementName() {
		return bloodElementName;
	}

	public void setBloodElementName(String bloodElementName) {
		this.bloodElementName = bloodElementName;
	}

	public String getBloodElementVolume() {
		return bloodElementVolume;
	}

	public void setBloodElementVolume(String bloodElementVolume) {
		this.bloodElementVolume = bloodElementVolume;
	}

	public String getBloodElementUnitCode() {
		return bloodElementUnitCode;
	}

	public void setBloodElementUnitCode(String bloodElementUnitCode) {
		this.bloodElementUnitCode = bloodElementUnitCode;
	}

	public String getBloodElementUnitName() {
		return bloodElementUnitName;
	}

	public void setBloodElementUnitName(String bloodElementUnitName) {
		this.bloodElementUnitName = bloodElementUnitName;
	}
	
	
	
	
}
